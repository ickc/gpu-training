import jax
import jax.numpy as jnp
import jax.scipy

import numpy as np

import matplotlib.pyplot as plt

# %%
MASK = jnp.array([
    [True, True, True],
    [True, False, True],
    [True, True, True],
])


# %%
@jax.jit
def next(state):
    n_neighbors = jax.scipy.signal.convolve(state, MASK, mode="same", precision="fastest").astype(jnp.int8)
    return jnp.where(
        (state & ((n_neighbors == 2) | (n_neighbors == 3))) |
        (~state & (n_neighbors == 3)),
        True,
        False,
    )


